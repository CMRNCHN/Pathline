@dataclass
class GapTask:
    """The atomic unit of agent work.

    Each instance represents one thing the agent does not know and wants to
    learn. The queue sorts by priority_score (descending).

    Phase 1 fields are all populated. Phase 2 placeholder fields are noted
    with # PHASE-2 comments; they default to None and are safe to ignore.
    """

    # -- Identity --
    gap_id: str = field(default_factory=lambda: str(uuid.uuid4()))
    """Stable UUID assigned at creation. Never changes."""

    gap_type: GapType = GapType.UNVISITED_ANNOUNCED_OPTION
    """Determines which engine generated this gap and how to work it."""

    status: GapStatus = GapStatus.PENDING
    """Current state in the GapStatus state machine."""

    # -- Targeting --
    target_ivr_id: str = ""
    """Which IVR system this gap belongs to (phone number or logical ID)."""

    target_node_key: str | None = None
    """Canonical PromptNode key (IvrMapper._nodes key) this gap targets.
    None for gaps that don't map to a specific existing node
    (e.g., HYPOTHESIS_TEST for a predicted but not-yet-seen node)."""

    target_option: str | None = None
    """DTMF option within the target node, if applicable.
    Set for UNVISITED_ANNOUNCED_OPTION, UNCONFIRMED_EDGE, VARIABLE_BEHAVIOR."""

    planned_path: list[str] = field(default_factory=list)
    """DTMF sequence to navigate from the IVR root to the target node.
    Populated by the path planner before dispatching. Empty = not yet planned."""

    # -- Priority --
    priority_score: float = 0.0
    """Computed priority in [0.0, 1.0]. Higher = worked first.
    Recalculated by the queue whenever sub-score inputs change."""

    score_breakdown: PriorityScoreBreakdown | None = None
    """Sub-score breakdown for debugging and operator visibility."""

    operator_priority: float = 0.5
    """Operator-assigned weight in [0.0, 1.0]. Default 0.5 (neutral)."""

    # -- Provenance --
    created_at: datetime = field(default_factory=lambda: datetime.now(timezone.utc))
    source_session_id: str | None = None
    """Session that triggered this gap (if observation-driven)."""

    source_engine: str | None = None
    """Which engine created this gap: 'discovery', 'validation', 'hypothesis',
    or 'operator'."""

    # -- Lifecycle tracking --
    updated_at: datetime = field(default_factory=lambda: datetime.now(timezone.utc))

    in_progress_since: datetime | None = None
    """When the gap entered IN_PROGRESS. Used to detect stuck sessions."""

    resolved_at: datetime | None = None
    deferred_until: datetime | None = None
    """If status == DEFERRED, re-activate when datetime.now(utc) >= deferred_until."""

    retry_count: int = 0
    """How many FAILED attempts before this gap was re-queued."""

    max_retries: int = 3
    """FAILED gaps beyond max_retries are not re-queued; they stay FAILED."""

    superseded_reason: str | None = None
    """Human-readable reason why this gap was superseded (e.g., 'node removed
    in structural update session-abc123')."""

    # -- Evidence collected during resolution --
    resolution_session_id: str | None = None
    """Session that resolved this gap."""

    resolution_notes: str | None = None
    """Free-form notes from the resolution pass (what was found)."""

    # -- Human/operator context --
    operator_context: str | None = None
    """Free-form text from the operator for MANUAL_INVESTIGATION gaps."""

    operator_label: str | None = None
    """Short label for display in the analyst UI."""

    # -- Gap-type-specific metadata (all optional) --
    observed_confidence: float | None = None
    """For AMBIGUOUS_PROMPT / CONFIDENCE_DECAY: the last recorded confidence."""

    variable_outcomes: list[str] = field(default_factory=list)
    """For VARIABLE_BEHAVIOR: list of distinct next_node_keys observed."""

    unreachable_count: int = 0
    """For DEPRECATED_CANDIDATE: how many consecutive times the node was
    unreachable."""

    hypothesis_id: str | None = None
    """For HYPOTHESIS_TEST / CROSS_IVR_PATTERN: reference to the hypothesis
    record that spawned this gap. PHASE-2: HypothesisEngine will populate."""

    # PHASE-2 placeholders (safe to ignore in Phase 1)
    extraction_config: dict[str, Any] | None = None
    # PHASE-2: ExtractionSetup config blob for EXTRACTION_SETUP gaps.

    auth_credential_ref: str | None = None
    # PHASE-2: Reference to a credential vault entry for POST_AUTH_UNKNOWN gaps.

    cross_ivr_pattern_ref: str | None = None
    # PHASE-2: Pattern library reference for CROSS_IVR_PATTERN gaps.

    # -- Heap ordering support --
    def __lt__(self, other: "GapTask") -> bool:
        # Higher score = higher priority; invert for min-heap.
        return self.priority_score > other.priority_score


# ---------------------------------------------------------------------------
# State machine transitions
# ---------------------------------------------------------------------------

class GapTaskTransitionError(Exception):
    pass


def transition(gap: GapTask, new_status: GapStatus, **kwargs: Any) -> None:
    """Apply a state transition, enforcing the state machine rules.

    Valid transitions:
      PENDING     → IN_PROGRESS   (session dispatched to work this gap)
      PENDING     → DEFERRED      (rate limit, time restriction, etc.)
      PENDING     → SUPERSEDED    (structural change obsoleted the gap)
      IN_PROGRESS → RESOLVED      (gap answered, knowledge updated)
      IN_PROGRESS → FAILED        (call dropped, auth blocked, etc.)
      IN_PROGRESS → DEFERRED      (session hit a hard stop mid-navigation)
      IN_PROGRESS → SUPERSEDED    (structural change while call was in flight)
      DEFERRED    → PENDING       (deferred_until elapsed; re-queued)
      DEFERRED    → SUPERSEDED    (structural change while gap was deferred)
      FAILED      → PENDING       (retry, if retry_count < max_retries)

    Terminal states: RESOLVED, SUPERSEDED.
    FAILED is terminal when retry_count >= max_retries.
    """
    current = gap.status
    allowed: dict[GapStatus, set[GapStatus]] = {
        GapStatus.PENDING: {
            GapStatus.IN_PROGRESS,
            GapStatus.DEFERRED,
            GapStatus.SUPERSEDED,
        },
        GapStatus.IN_PROGRESS: {
            GapStatus.RESOLVED,
            GapStatus.FAILED,
            GapStatus.DEFERRED,
            GapStatus.SUPERSEDED,
        },
        GapStatus.DEFERRED: {
            GapStatus.PENDING,
            GapStatus.SUPERSEDED,
        },
        GapStatus.FAILED: {
            GapStatus.PENDING,  # retry only
        },
        GapStatus.RESOLVED: set(),   # terminal
        GapStatus.SUPERSEDED: set(), # terminal
    }
    if new_status not in allowed.get(current, set()):
        raise GapTaskTransitionError(
            f"Invalid transition: {current} → {new_status} for gap {gap.gap_id}"
        )
    if new_status == GapStatus.FAILED and gap.retry_count >= gap.max_retries:
        # FAILED with no retries left: stays terminal, block re-queue attempts.
        pass
    now = datetime.now(timezone.utc)
    gap.status = new_status
    gap.updated_at = now
    if new_status == GapStatus.IN_PROGRESS:
        gap.in_progress_since = now
    elif new_status == GapStatus.RESOLVED:
        gap.resolved_at = now
        gap.resolution_session_id = kwargs.get("session_id")
        gap.resolution_notes = kwargs.get("notes")
    elif new_status == GapStatus.DEFERRED:
        gap.deferred_until = kwargs.get("until")
    elif new_status == GapStatus.SUPERSEDED:
        gap.superseded_reason = kwargs.get("reason", "structural change")
    elif new_status == GapStatus.PENDING and current == GapStatus.FAILED:
        gap.retry_count += 1


# ---------------------------------------------------------------------------
# GapTaskQueue — the agent's cognitive agenda
# ---------------------------------------------------------------------------

class GapTaskQueue:
    """Priority queue of GapTasks sorted by priority_score (highest first).

    This is the agent's cognitive agenda. The loop is:
        while not queue.done():
            gap = queue.pop()
            result = session_runner.execute(gap)
            queue.handle_result(gap, result)

    Thread-safety: not thread-safe by default. Wrap in a lock for concurrent
    session dispatch.
    """

    DEFAULT_THRESHOLD: float = 0.10
    """Gaps below this priority_score are considered not worth working.
    All-remaining-below-threshold is one of the two done() conditions."""

    def __init__(self, threshold: float = DEFAULT_THRESHOLD) -> None:
        self._heap: list[GapTask] = []
        self._all: dict[str, GapTask] = {}  # gap_id → GapTask (all states)
        self._threshold = threshold

    def push(self, gap: GapTask) -> None:
        """Add a new PENDING gap. Raises if a gap with the same ID already exists."""
        if gap.gap_id in self._all:
            raise ValueError(f"Gap {gap.gap_id} already in queue")
        self._all[gap.gap_id] = gap
        if gap.status == GapStatus.PENDING:
            heapq.heappush(self._heap, gap)

    def pop(self) -> GapTask | None:
        """Return the highest-priority PENDING gap, or None if none available.

        Skips gaps that are no longer PENDING (they may have been superseded
        or transitioned externally while on the heap).
        """
        while self._heap:
            gap = heapq.heappop(self._heap)
            if gap.status == GapStatus.PENDING:
                return gap
        return None

    def requeue(self, gap: GapTask) -> None:
        """Push a gap back onto the heap (after DEFERRED→PENDING or FAILED→PENDING).

        The gap must already be in self._all and have status PENDING.
        """
        if gap.gap_id not in self._all:
            raise ValueError(f"Gap {gap.gap_id} not known to this queue")
        heapq.heappush(self._heap, gap)

    def supersede_by_node(self, node_key: str, reason: str) -> list[str]:
        """Supersede all non-terminal gaps targeting node_key.

        Called when IvrMapper detects that a node has been removed or
        fundamentally restructured. Returns list of superseded gap IDs.
        """
        superseded_ids: list[str] = []
        for gap in self._all.values():
            if (
                gap.target_node_key == node_key
                and gap.status not in (GapStatus.RESOLVED, GapStatus.SUPERSEDED)
            ):
                transition(gap, GapStatus.SUPERSEDED, reason=reason)
                superseded_ids.append(gap.gap_id)
        return superseded_ids

    def activate_deferred(self) -> list[GapTask]:
        """Re-activate all DEFERRED gaps whose deferred_until has elapsed."""
        now = datetime.now(timezone.utc)
        reactivated: list[GapTask] = []
        for gap in self._all.values():
            if (
                gap.status == GapStatus.DEFERRED
                and gap.deferred_until is not None
                and now >= gap.deferred_until
            ):
                transition(gap, GapStatus.PENDING)
                self.requeue(gap)
                reactivated.append(gap)
        return reactivated

    # -- Cognitive query interface --

    def unknown_territory(self) -> list[GapTask]:
        """What do I know NOTHING about?

        Returns PENDING gaps of types that represent complete absence of
        knowledge: UNVISITED_ANNOUNCED_OPTION, HYPOTHESIS_TEST,
        POST_AUTH_UNKNOWN, TIMEOUT_PATH_UNKNOWN.
        """
        zero_knowledge_types = {
            GapType.UNVISITED_ANNOUNCED_OPTION,
            GapType.HYPOTHESIS_TEST,
            GapType.POST_AUTH_UNKNOWN,
            GapType.TIMEOUT_PATH_UNKNOWN,
        }
        return [
            g for g in self._all.values()
            if g.status == GapStatus.PENDING and g.gap_type in zero_knowledge_types
        ]

    def uncertain_territory(self) -> list[GapTask]:
        """What do I THINK I know but might be wrong about?

        Returns PENDING gaps of types that represent degraded or contested
        knowledge: CONFIDENCE_DECAY, STRUCTURAL_DRIFT, CONTENT_DRIFT,
        VARIABLE_BEHAVIOR, UNCONFIRMED_EDGE.
        """
        uncertain_types = {
            GapType.CONFIDENCE_DECAY,
            GapType.STRUCTURAL_DRIFT,
            GapType.CONTENT_DRIFT,
            GapType.VARIABLE_BEHAVIOR,
            GapType.UNCONFIRMED_EDGE,
        }
        return [
            g for g in self._all.values()
            if g.status == GapStatus.PENDING and g.gap_type in uncertain_types
        ]

    def highest_value_next(self) -> GapTask | None:
        """What is the most valuable thing to learn next?

        Peeks at the top of the heap without popping. Returns the highest
        priority_score PENDING gap, or None.
        """
        # Drain stale entries to find the live top.
        while self._heap and self._heap[0].status != GapStatus.PENDING:
            heapq.heappop(self._heap)
        return self._heap[0] if self._heap else None

    def pending_count(self) -> int:
        return sum(1 for g in self._all.values() if g.status == GapStatus.PENDING)

    def in_progress_count(self) -> int:
        return sum(1 for g in self._all.values() if g.status == GapStatus.IN_PROGRESS)

    def done(self) -> bool:
        """Is the agent done?

        Two exit conditions:
        1. No PENDING or IN_PROGRESS gaps remain (queue fully drained).
        2. All remaining PENDING gaps are below the priority threshold AND
           no gaps are currently IN_PROGRESS.

        Condition 2 prevents premature done() while a session is active.
        Condition 1 is the clean termination case.

        The threshold is configurable at construction time.
        """
        pending = [g for g in self._all.values() if g.status == GapStatus.PENDING]
        in_progress = [g for g in self._all.values() if g.status == GapStatus.IN_PROGRESS]

        if in_progress:
            # A session is active; we are not done.
            return False

        if not pending:
            # Nothing left to do.
            return True

        # Done if every remaining gap is below the threshold.
        return all(g.priority_score < self._threshold for g in pending)

    def summary(self) -> dict[str, int]:
        """Status counts for logging and operator UI."""
        counts: dict[str, int] = {s: 0 for s in GapStatus}
        for gap in self._all.values():
            counts[gap.status] += 1
        return counts
