"""GapTask system — the cognitive agenda of the IVR mapping agent.

The agent's entire behavior is:
    pop highest-priority gap → go answer it → update IvrMapper → repeat

This module defines:
- GapType enum (all 14 gap types, three sources)
- GapStatus state machine (PENDING → IN_PROGRESS → terminal)
- GapTask dataclass (all fields for Phase 1, placeholders for Phase 2+)
- GapTaskQueue (priority queue + done() termination logic)
- Priority scoring sub-components (all normalized to [0.0, 1.0])

STATUS: Phase 1 design — no engine wiring yet.
DiscoveryEngine, ValidationEngine, HypothesisEngine are aspirational
components that WILL produce GapTasks; they do not exist in the codebase
as of 2026-05-31. The queue and dataclass are implementation-ready.
"""

from __future__ import annotations

import heapq
import uuid
from dataclasses import dataclass, field
from datetime import datetime, timezone
from enum import StrEnum
from typing import Any


# ---------------------------------------------------------------------------
# Gap taxonomy
# ---------------------------------------------------------------------------

class GapType(StrEnum):
    # -- OBSERVATION-DRIVEN (generated by DiscoveryEngine from SessionObservations) --

    UNVISITED_ANNOUNCED_OPTION = "unvisited_announced_option"
    """Option was announced in a prompt (present in PromptNode.announced_options)
    but has never been pressed. The most common gap type in early exploration."""

    UNCONFIRMED_EDGE = "unconfirmed_edge"
    """Edge traversed exactly once (BranchObservation.count == 1). Needs a second
    confirmation pass to raise confidence above the single-observation floor."""

    VARIABLE_BEHAVIOR = "variable_behavior"
    """Same DTMF input at the same node yielded different next-node outcomes on
    different calls. Indicates time-of-day routing, load balancing, or a bug."""

    AMBIGUOUS_PROMPT = "ambiguous_prompt"
    """Prompt transcript is present but PromptClassification.confidence < 0.50.
    The parser could not reliably determine intent or extract options."""

    TIMEOUT_PATH_UNKNOWN = "timeout_path_unknown"
    """What the IVR does if no DTMF is pressed within its timeout window has never
    been observed for this node. Some IVRs repeat; others route to agent."""

    POST_AUTH_UNKNOWN = "post_auth_unknown"
    """A subtree sits behind an authentication gate (PromptIntent.AUTH_GATE,
    Phase 2 node type) that has never been successfully traversed."""

    # -- VALIDATION-DRIVEN (generated by ValidationEngine) --

    STRUCTURAL_DRIFT = "structural_drift"
    """Node was reached on a recent call but its branch structure differs from
    what is stored in the IvrMapper graph. Options have been added or removed."""

    CONTENT_DRIFT = "content_drift"
    """Node was reached and its structure matches, but the prompt wording differs
    significantly from the stored canonical text (edit distance > threshold)."""

    CONFIDENCE_DECAY = "confidence_decay"
    """Node's stored confidence has fallen below the reconfirmation threshold due
    to elapsed time without a fresh observation. Knowledge is going stale."""

    DEPRECATED_CANDIDATE = "deprecated_candidate"
    """Node has been unreachable on 2+ consecutive attempts. May have been removed
    from the IVR. Needs one more confirmation before marking as deprecated."""

    # -- HYPOTHESIS-DRIVEN (generated by HypothesisEngine) --

    HYPOTHESIS_TEST = "hypothesis_test"
    """Navigate to a predicted location to confirm or refute a structural
    hypothesis (e.g., "billing path likely ends in HUMAN_HANDOFF after 3 hops")."""

    CROSS_IVR_PATTERN = "cross_ivr_pattern"
    """Test whether a pattern observed in other mapped IVRs applies here
    (e.g., pressing 0 at any menu routes to operator)."""

    # -- HUMAN-GENERATED --

    MANUAL_INVESTIGATION = "manual_investigation"
    """Operator flagged a node or path for deeper investigation. Free-form
    context stored in GapTask.operator_context."""

    EXTRACTION_SETUP = "extraction_setup"
    """Operator wants to configure status-extraction automation for a specific
    node (e.g., "extract account balance from this prompt"). Phase 2 feature."""


# ---------------------------------------------------------------------------
# Gap status state machine
# ---------------------------------------------------------------------------

class GapStatus(StrEnum):
    PENDING = "pending"
    """Waiting to be dispatched to a call session."""

    IN_PROGRESS = "in_progress"
    """A call session is actively navigating toward this gap's target."""

    RESOLVED = "resolved"
    """Gap was answered — knowledge updated, confidence raised."""

    FAILED = "failed"
    """Navigation attempt failed (call dropped, auth blocked, etc.).
    Retryable: a new GapTask may be created with retry_count incremented."""

    DEFERRED = "deferred"
    """Gap cannot be worked right now (e.g., rate limit, time-of-day
    restriction). Will be re-activated when deferred_until elapses."""

    SUPERSEDED = "superseded"
    """A structural change (node removed, path changed) made this gap
    irrelevant. Can be reached from PENDING, IN_PROGRESS, or DEFERRED."""


# ---------------------------------------------------------------------------
# Priority scoring
# ---------------------------------------------------------------------------

@dataclass(frozen=True)
class PriorityScoreBreakdown:
    """All sub-scores normalized to [0.0, 1.0]. Final score also in [0.0, 1.0]."""

    impact_score: float
    """Does this path lead toward a high-value endpoint?

    Computed by graph reachability from the gap's target_node_key:
    - 1.0  if the target node IS a human-handoff / extraction point
    - 0.85 if a known human-handoff or extraction node is within 2 hops
    - 0.60 if within 3–5 hops
    - 0.30 if reachable but > 5 hops or unknown distance
    - 0.10 if the path leads to a known dead-end (no outbound edges)
    - 0.05 default when reachability cannot be computed (unexplored subtree)

    Weight: 0.35
    """

    urgency_score: float
    """How stale or time-sensitive is this gap?

    Computed from age and gap type:
    - MANUAL_INVESTIGATION / EXTRACTION_SETUP: urgency = operator_priority (see below)
    - STRUCTURAL_DRIFT / CONTENT_DRIFT: urgency = min(1.0, days_since_last_visit / 7)
    - CONFIDENCE_DECAY: urgency = 1.0 - current_confidence  (lower confidence → more urgent)
    - DEPRECATED_CANDIDATE: urgency = min(1.0, unreachable_count / 3)
    - All others: urgency = min(1.0, days_since_created / 30)

    Weight: 0.25
    """

    confidence_benefit: float
    """How much does filling this gap improve the world model?

    Named "confidence_penalty" in the spec — here clarified as the BENEFIT of
    filling the gap (a low-confidence or unknown node has HIGH benefit, so the
    weight is positive and the score increases priority).

    Computed:
    - UNVISITED_ANNOUNCED_OPTION: 1.0 (zero knowledge of this branch)
    - POST_AUTH_UNKNOWN: 1.0 (entire subtree unknown)
    - HYPOTHESIS_TEST / CROSS_IVR_PATTERN: 0.7 (structured uncertainty)
    - UNCONFIRMED_EDGE: 1.0 - BranchObservation.confidence
    - AMBIGUOUS_PROMPT: 1.0 - PromptClassification.confidence (clamped to [0,1])
    - CONFIDENCE_DECAY: 1.0 - PromptNode.confidence
    - STRUCTURAL_DRIFT / CONTENT_DRIFT / VARIABLE_BEHAVIOR: 0.8
    - DEPRECATED_CANDIDATE: 0.5 (partial knowledge, outcome uncertain)
    - MANUAL_INVESTIGATION: 0.6
    - EXTRACTION_SETUP: 0.4 (node is known; setup is administrative)
    - TIMEOUT_PATH_UNKNOWN: 0.9

    Weight: 0.20
    """

    user_priority: float
    """Operator-assigned priority in [0.0, 1.0]. Default 0.5 (neutral).

    Operators can override to 1.0 ("investigate immediately") or 0.0
    ("deprioritize"). Stored as GapTask.operator_priority.

    Weight: 0.15
    """

    freshness_bonus: float
    """Recently discovered gaps get a small boost to avoid starvation.

    freshness_bonus = max(0.0, 1.0 - (hours_since_created / 24))

    Decays linearly from 1.0 at creation to 0.0 after 24 hours.

    Weight: 0.05
    """

    @property
    def final_score(self) -> float:
        return (
            0.35 * self.impact_score
            + 0.25 * self.urgency_score
            + 0.20 * self.confidence_benefit
            + 0.15 * self.user_priority
            + 0.05 * self.freshness_bonus
        )


def example_high_priority_score() -> PriorityScoreBreakdown:
    """Example: unexplored option on the billing path of a major bank.

    Scenario: PromptNode at "For billing press 1" — option "1" is in
    announced_options but BranchObservation.count == 0. The billing path
    in similar IVRs ends in a HUMAN_HANDOFF within 2 hops.

    Sub-scores:
    - impact_score    = 0.85  (human-handoff within 2 hops via cross-IVR pattern)
    - urgency_score   = 0.80  (gap created 24 days ago, min(1.0, 24/30))
    - confidence_benefit = 1.0  (UNVISITED_ANNOUNCED_OPTION: zero knowledge)
    - user_priority   = 0.80  (operator marked billing paths as high priority)
    - freshness_bonus = 0.0   (24+ hours old)

    final = 0.35*0.85 + 0.25*0.80 + 0.20*1.0 + 0.15*0.80 + 0.05*0.0
          = 0.2975   + 0.2000   + 0.2000   + 0.1200   + 0.0000
          = 0.817
    """
    return PriorityScoreBreakdown(
        impact_score=0.85,
        urgency_score=0.80,
        confidence_benefit=1.0,
        user_priority=0.80,
        freshness_bonus=0.0,
    )


def example_low_priority_score() -> PriorityScoreBreakdown:
    """Example: timeout behavior on a rarely-used submenu.

    Scenario: TIMEOUT_PATH_UNKNOWN gap for a node deep in the "fax number
    update" submenu. Node is visited by < 1% of real callers. No downstream
    human-handoff within known graph. Gap is 2 hours old.

    Sub-scores:
    - impact_score    = 0.10  (leads to a dead-end subtree, no high-value endpoint)
    - urgency_score   = 0.003 (min(1.0, 2/30 days ≈ 0.003))
    - confidence_benefit = 0.9  (TIMEOUT_PATH_UNKNOWN)
    - user_priority   = 0.20  (operator explicitly deprioritized fax subtree)
    - freshness_bonus = 0.92  (2 hours old: 1.0 - 2/24 ≈ 0.92)

    final = 0.35*0.10 + 0.25*0.003 + 0.20*0.9 + 0.15*0.20 + 0.05*0.92
          = 0.0350   + 0.0008     + 0.1800   + 0.0300   + 0.0460
          = 0.292

    The freshness_bonus temporarily inflates this; after 24h it falls to ~0.22.
    """
    return PriorityScoreBreakdown(
        impact_score=0.10,
        urgency_score=0.003,
        confidence_benefit=0.9,
        user_priority=0.20,
        freshness_bonus=0.92,
    )


